                return Storage::response('public/pdf/'.$id, $id, [
                    'Content-Type' => 'application/pdf',
                    'Content-Disposition' => 'inline; filename="' . $id . '"'
                ]);
            }
        }
        catch(Exception $e)
        {
            return response(['message' => $e->getMessage()], 404);
